// Programa de ecuaciones aritmeticas con dos numeros

let num1 = prompt("Ingresar un numero mayor a cero");
let num2 = prompt("Ingresar un numero distinto al anterior mayor a cero");

let suma = parseInt(num1) + parseInt(num2);
let resta = parseInt(num1) - parseInt(num2);
let division = parseInt(num1) / parseInt(num2);
let multiplicacion = parseInt(num1) * parseInt(num2);
let modulolo = parseInt(num1) % parseInt(num2);

document.write(`El resultado de la suma es ${suma}, `);
document.write(`el resultado de la resta es ${resta}, `);
document.write(`el resultado de la division es ${division}, `);
document.write(`el resultado de la multiplicacion es ${multiplicacion}, `);
document.write(`el resultado del modulo es ${modulolo} . `);




